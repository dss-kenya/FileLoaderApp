package com.fileloader.android.model;

public class ImageUrl {
    String raw;
    String full;
    String small;
    String regular;
    String thumb;

    public String getRaw() {
        return raw;
    }

    public String getFull() {
        return full;
    }

    public String getRegular() {
        return regular;
    }

    public String getSmall() {
        return small;
    }

    public String getThumb() {
        return thumb;
    }
}
